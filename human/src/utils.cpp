vec4 hex2_vec(int64_t hex)
{
    return vec4({
        (hex >> 16 & 0xFF) / 255.0f,
        (hex >> 8 & 0xFF) / 255.0f,
        (hex & 0xFF) / 255.0f,
        1
    });
}

vec2 mouse_pos_to_clip_space(const vec2d& pos, int winWidth, int winHeight)
{
    vec2 mouse = vec2({static_cast<float>(pos[0]) / winWidth, static_cast<float>(pos[1]) / winHeight}) * 2.0f - 1.0f;
    mouse[1] = -mouse[1];
    return mouse;
}
